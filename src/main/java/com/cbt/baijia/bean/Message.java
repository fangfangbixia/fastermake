package com.cbt.baijia.bean;

import java.util.Arrays;
import java.util.List;

public class Message {
   private String app_id;
   private String app_token;
   private String title;
   private String content;
   private String origin_url;
   private String cover_images;
   private Integer is_original;

    public String getApp_id() {
        return app_id;
    }

    public void setApp_id(String app_id) {
        this.app_id = app_id;
    }

    public String getApp_token() {
        return app_token;
    }

    public void setApp_token(String app_token) {
        this.app_token = app_token;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getOrigin_url() {
        return origin_url;
    }

    public void setOrigin_url(String origin_url) {
        this.origin_url = origin_url;
    }

    public String getCover_images() {
        return cover_images;
    }

    public void setCover_images(String cover_images) {
        this.cover_images = cover_images;
    }

    public Integer getIs_original() {
        return is_original;
    }

    public void setIs_original(Integer is_original) {
        this.is_original = is_original;
    }


    @Override
    public String toString() {
        return "Message{" +
                "app_id='" + app_id + '\'' +
                ", app_token='" + app_token + '\'' +
                ", title='" + title + '\'' +
                ", content='" + content + '\'' +
                ", origin_url='" + origin_url + '\'' +
                ", cover_images=" + cover_images +
                ", is_original=" + is_original +
                '}';
    }
}
